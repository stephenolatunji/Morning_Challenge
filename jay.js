function myBio(name, location, profession, hobby) {
    return `My name is ${name}, I am based in ${location}. I am an accomplished ${profession}, who likes to play ${hobby}.`
}
console.log(myBio("Stephen", "Lagos, Nigeria", "Software Developer", "Scrabble"));
console.log(myBio("Olatunji", "Denver, Colorado", "Fine Artist", "Chess"));
console.log(myBio("McKorr", "Paris, France", "Photographer", "Rugby"));
